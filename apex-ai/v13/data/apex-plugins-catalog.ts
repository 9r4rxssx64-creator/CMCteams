export interface ApexPluginManifest {
  id: string;
  name: string;
  source: PluginSource;
  url: string;
  description: string;
  category: PluginCategory;
  /** Browser PWA peut consommer (REST / fetch direct) */
  pwa_compatible: boolean;
  /** Nécessite OAuth (Apex peut le déclencher mais user doit signer) */
  oauth_required: boolean;
  /** Clé API (champ vault `ax_<service>_key`) */
  api_key_service?: string;
  /** Statut courant côté Apex */
  status: PluginStatus;
  /** Tools registry IDs créés/exposés via apex-tools après install */
  apex_tools?: string[];
  /** Méthode d'install runtime */
  install_method?: 'native-fetch' | 'cdn-lazy' | 'cloudflare-worker' | 'app-native' | 'web-link';
  /** Valeur estimée pour Apex (priorité d'install) */
  estimated_value: 'critical' | 'high' | 'medium' | 'low';
  /** Tags libres pour filtrage */
  tags?: string[];
}

